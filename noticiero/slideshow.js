const HIDE_CONTROLS_DELAY = 2000;

function showFloatingControls(){
  const root = qs('.slideshow');
  if(!root) return;
  root.classList.remove('controls-hidden');
}

function scheduleHideFloatingControls(){
  const root = qs('.slideshow');
  if(!root) return;
  clearTimeout(_hideControlsTimer);
  _hideControlsTimer = setTimeout(()=>{
    root.classList.add('controls-hidden');
  }, HIDE_CONTROLS_DELAY);
}

function enableFullscreenControlAutoHide(){
  const root = qs('.slideshow');
  if(!root) return;
  // ensure controls visible initially and schedule hide
  showFloatingControls();
  scheduleHideFloatingControls();
  const onUserActivity = ()=>{ showFloatingControls(); scheduleHideFloatingControls(); };
  // store handler so it can be removed later
  root._onFullScreenUserActivity = onUserActivity;
  document.addEventListener('mousemove', onUserActivity);
  document.addEventListener('touchstart', onUserActivity);
  document.addEventListener('keydown', onUserActivity);
}

function disableFullscreenControlAutoHide(){
  const root = qs('.slideshow');
  if(!root) return;
  clearTimeout(_hideControlsTimer);
  root.classList.remove('controls-hidden');
  if(root._onFullScreenUserActivity){
    document.removeEventListener('mousemove', root._onFullScreenUserActivity);
    document.removeEventListener('touchstart', root._onFullScreenUserActivity);
    document.removeEventListener('keydown', root._onFullScreenUserActivity);
    delete root._onFullScreenUserActivity;
  }
}

document.addEventListener('fullscreenchange', ()=>{
  const root = qs('.slideshow');
  const btn = qs('#fullscreen');
  if(document.fullscreenElement){
    root.classList.add('fullscreen');
    if(btn) btn.textContent = 'Exit Fullscreen';
    enableFullscreenControlAutoHide();
  } else {
    root.classList.remove('fullscreen');
    if(btn) btn.textContent = 'Fullscreen';
    disableFullscreenControlAutoHide();
  }
  // After toggling fullscreen state, adjust the image sizing immediately
  scaleImageToFill();
});

// keyboard shortcuts: F to toggle fullscreen, arrows/Home/End to navigate
window.addEventListener('keydown', (e)=>{
  // ignore when typing in inputs
  if (e.target && (e.target.tagName === 'INPUT' || e.target.tagName === 'TEXTAREA' || e.target.isContentEditable)) return;
  if (e.metaKey || e.ctrlKey || e.altKey) return;

  if (e.key === 'f' || e.key === 'F') {
    toggleFullscreen();
  } else if (e.key === 'ArrowRight') {
    pause(); next();
  } else if (e.key === 'ArrowLeft') {
    pause(); prev();
  } else if (e.key === 'Home') {
    pause(); showImage(0);
  } else if (e.key === 'End') {
    pause(); showImage(IMAGES.length - 1);
  }
});

function updateNavButtons(){
  const prevBtn = qs('#prev');
  const nextBtn = qs('#next');
  if(prevBtn) prevBtn.disabled = (idx <= 0);
  if(nextBtn) nextBtn.disabled = (idx >= IMAGES.length - 1);
}

function scaleImageToFill(){
  const img = qs('#main-img');
  const viewer = qs('.viewer');
  if(!img || !viewer) return;

  // If not in fullscreen, reset inline sizing and let CSS handle layout
  if(!document.fullscreenElement){
    img.style.width = '';
    img.style.height = '';
    img.style.objectFit = '';
    return;
  }

  const natW = img.naturalWidth || img.width;
  const natH = img.naturalHeight || img.height;
  if(!natW || !natH) return;

  const vw = viewer.clientWidth;
  const vh = viewer.clientHeight;

  // Compute scale to fit the viewer, allowing scaling up (scale >= 1) or down
  const scale = Math.min(vw / natW, vh / natH);
  const newW = Math.round(natW * scale);
  const newH = Math.round(natH * scale);

  img.style.width = newW + 'px';
  img.style.height = newH + 'px';
  // Use objectFit none because we're explicitly sizing the image element
  img.style.objectFit = 'none';
}

// Recompute size on resize (small throttle)
let _resizeTimer = null;
window.addEventListener('resize', ()=>{ clearTimeout(_resizeTimer); _resizeTimer = setTimeout(scaleImageToFill, 60); });

function buildThumbnails(){
  const row = qs('.thumbnail-row');
  IMAGES.forEach((f,i)=>{
    const img = document.createElement('img');
    img.className='thumbnail';
    img.loading='lazy';
    img.src = encodeURI('images/' + f);
    img.title = f;
    img.addEventListener('click', ()=>{ pause(); showImage(i); window.scrollTo({top:0,behavior:'smooth'}); });
    row.appendChild(img);
  });
}

window.addEventListener('DOMContentLoaded', ()=>{
  if(IMAGES.length===0){ qs('.viewer').textContent='No images found.'; return; }
  buildThumbnails();
  showImage(0);
  qs('#next').addEventListener('click', ()=>{ pause(); next();});
  qs('#prev').addEventListener('click', ()=>{ pause(); prev();});
  const _playBtn = qs('#play');
  if(_playBtn){ _playBtn.addEventListener('click', ()=>{ togglePlay();}); }
  const fsBtn = qs('#fullscreen');
  if(fsBtn){ fsBtn.addEventListener('click', ()=>{ toggleFullscreen(); }); }

  // Touch swipe support for mobile: swipe left/right in the viewer to navigate slides
  (function enableTouchSwipe(){
    const viewerEl = qs('.viewer');
    if(!viewerEl) return;
    let touchStartX = null;
    let touchStartY = null;
    let touchMoved = false;
    const TOUCH_SWIPE_THRESHOLD = 50; // pixels

    viewerEl.addEventListener('touchstart', (e)=>{
      if(e.touches && e.touches.length === 1){
        const t = e.touches[0];
        touchStartX = t.clientX;
        touchStartY = t.clientY;
        touchMoved = false;
      }
    }, {passive:true});

    viewerEl.addEventListener('touchmove', (e)=>{
      if(touchStartX === null) return;
      if(e.touches && e.touches.length === 1){
        const t = e.touches[0];
        const dx = t.clientX - touchStartX;
        const dy = t.clientY - touchStartY;
        if(Math.abs(dx) > Math.abs(dy) && Math.abs(dx) > 10){
          // horizontal swipe detected  prevent vertical page scroll while swiping horizontally
          e.preventDefault();
          touchMoved = true;
        }
      }
    }, {passive:false});

    viewerEl.addEventListener('touchend', (e)=>{
      if(touchStartX === null) return;
      const t = (e.changedTouches && e.changedTouches[0]) || null;
      if(!t){ touchStartX = null; touchStartY = null; touchMoved=false; return; }
      const dx = t.clientX - touchStartX;
      const dy = t.clientY - touchStartY;
      if(Math.abs(dx) > Math.abs(dy) && Math.abs(dx) > TOUCH_SWIPE_THRESHOLD){
        if(dx < 0){ pause(); next(); } else { pause(); prev(); }
      }
      touchStartX = null;
      touchStartY = null;
      touchMoved = false;
    }, {passive:true});

    viewerEl.addEventListener('touchcancel', ()=>{
      touchStartX = null;
      touchStartY = null;
      touchMoved = false;
    }, {passive:true});
  })();

  // slide number input: Enter to jump (1-indexed). Out-of-range numbers do nothing.
  const counterInput = qs('#counter-input');
  if(counterInput){
    counterInput.addEventListener('keydown', (e)=>{
      if(e.key === 'Enter'){
        const n = parseInt(counterInput.value, 10);
        if(Number.isInteger(n) && n >= 1 && n <= IMAGES.length){
          pause();
          showImage(n-1);
        } else {
          // out of range: reset to current
          counterInput.value = idx+1;
        }
      }
    });
    counterInput.addEventListener('blur', ()=>{ counterInput.value = idx+1; });
  }
});
