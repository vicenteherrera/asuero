let timer = null;
const delay = 2500;

function qs(sel){return document.querySelector(sel)}
function qsa(sel){return Array.from(document.querySelectorAll(sel))}

function showImage(i){
  const img = qs('#main-img');
  if(!IMAGES[i]) return;
  img.src = encodeURI('images/' + IMAGES[i]);
  // ensure sizing updates when the image loads (and immediately if cached)
  const _onLoad = ()=>{ scaleImageToFill(); img.removeEventListener('load', _onLoad); };
  img.addEventListener('load', _onLoad);
  if(img.complete) scaleImageToFill();

  // update counter input and total
  const counterInput = qs('#counter-input');
  const counterTotal = qs('.counter-total');
  if(counterInput){ counterInput.value = i+1; }
  if(counterTotal){ counterTotal.textContent = '/ ' + IMAGES.length; }

  qsa('.thumbnail').forEach((t,ti)=>t.classList.toggle('active', ti===i));
  const progress = Math.round(((i+1)/IMAGES.length)*100);
  qs('.progress > i').style.width = progress + '%';
  idx = i;
  updateNavButtons();

  // Keep the active thumbnail visible in the thumbnail row
  const row = qs('.thumbnail-row');
  const thumbs = qsa('.thumbnail');
  const activeThumb = thumbs[i];
  if(row && activeThumb && row.offsetParent !== null){
    // Prefer smooth native scrolling when available
    try{
      activeThumb.scrollIntoView({behavior:'smooth', inline:'center', block:'nearest'});
    }catch(e){
      // Fallback: manual adjustment
      const rect = activeThumb.getBoundingClientRect();
      const rowRect = row.getBoundingClientRect();
      row.scrollLeft += (rect.left - rowRect.left) - (row.clientWidth/2) + (rect.width/2);
    }
  }
}

function next(){
  // advance only if not at last slide; if autoplaying and at end, pause
  if (idx >= IMAGES.length - 1) {
    if (playing) pause();
    return;
  }
  showImage(idx + 1);
}
function prev(){
  // go back only if not at first slide
  if (idx <= 0) return;
  showImage(idx - 1);
}

function play(){ if(playing) return; playing=true; const _playBtn = qs('#play'); if(_playBtn) _playBtn.textContent='Pause'; timer = setInterval(next, delay); }
function pause(){ if(!playing) return; playing=false; const _playBtn = qs('#play'); if(_playBtn) _playBtn.textContent='Play'; clearInterval(timer); timer=null; }

function togglePlay(){ playing?pause():play(); }

/* Fullscreen helpers */
function toggleFullscreen(){
  const root = qs('.slideshow');
  if(!document.fullscreenElement){
    if(root.requestFullscreen) root.requestFullscreen();
    else if(root.webkitRequestFullscreen) root.webkitRequestFullscreen();
  } else {
    if(document.exitFullscreen) document.exitFullscreen();
    else if(document.webkitExitFullscreen) document.webkitExitFullscreen();
  }
}

